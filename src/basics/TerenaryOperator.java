package basics;

public class TerenaryOperator {
    public static void main(String[] args) {
        int x=5,y=6;
        x+=4; //x=x+4 
        System.out.println(x);
        y+=6; //y=y+6
        System.out.println(y);

        
        x*=4; //x=x*4
        System.out.println(x);
        y/=6; //y=y/6
        System.out.println(y);

    }
    
}
